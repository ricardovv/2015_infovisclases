window.onload = function() {
    var canvas1 = document.getElementById("canvas1"),
        context1 = canvas1.getContext("2d"),
        width = canvas1.width = window.innerWidth,
        height = canvas1.height = window.innerHeight/2;
        
        for (var i=0; i <100; i++) {
        context1.beginPath();
        context1.moveTo(0,0);
        context1.lineTo(300+i,150*i);
        context1.stroke();
        }

// / / / / / / / / / / / / / / / /
 
    var canvas2 = document.getElementById("canvas2"),
        context2 = canvas2.getContext("2d"),
        width = canvas2.width = window.innerWidth,
        height = canvas2.height = window.innerHeight/2;
        
        for (var i=0; i <100; i++) {
            if(i ==  i%20){
                context2.beginPath();
                context2.moveTo(10*i, 2*i);
                context2.lineTo(300+i,150*i);
                context2.stroke();
            }
        }


};//end onLoad

